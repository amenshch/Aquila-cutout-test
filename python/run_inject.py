BEAM500 = 36.3   # arcsec, 500um beam -- used for separation criterion


def _stamp_files_from_tag(tag, root):
    """Build stamp_files dict and surfdens_stamp path from a model tag and
    STAMP_ROOT, using the directory tree:
        <root>/cSD_{i:02d}/tBE_{j:02d}/{k:02d}/nc.<band>um.bs.<res>x0.rs3p0as.fits

    Band beam tokens (fixed Herschel PSF):
        160 um -> r13p5,  250 um -> r18p2,
        350 um -> r24p9,  500 um -> r36p3
    070 um is always None.  surfdens uses the 160 um token (r13p5).
    """
    import re
    m = re.fullmatch(r'i(\d+)j(\d+)k(\d+)', tag)
    if m is None:
        raise ValueError(f'Cannot parse model tag "{tag}": expected iNjMkK')
    i, j, k = int(m.group(1)), int(m.group(2)), int(m.group(3))
    subdir = os.path.join(root, f'cSD_{i:02d}', f'tBE_{j:02d}', f'{k:02d}')
    res = {'000': 'r13p5', '160': 'r13p5', '250': 'r18p2', '350': 'r24p9', '500': 'r36p3'}
    stamp_files = {'070': None}
    for b, r in res.items():
        if b == '000':
            stamp_files[b] = os.path.join(subdir, 'nc.surfdens.bs.r13p5x0.rs3p0as.fits')
        else:
            stamp_files[b] = os.path.join(subdir, f'nc.{b}um.bs.{r}x0.rs3p0as.fits')
    return stamp_files


def _normalise_model_entry(entry, stamp_root):
    """Accept a model entry as a bare tag string, a tag-only dict, or a full
    dict with stamp_files.  In the first two cases stamp paths are built
    automatically from STAMP_ROOT; a full dict is returned unchanged."""
    if isinstance(entry, str):
        entry = dict(tag=entry)
    if 'stamp_files' not in entry:
        if stamp_root is None:
            raise ValueError(
                f'Model "{entry["tag"]}" has no stamp_files and STAMP_ROOT is not set.')
        sf = _stamp_files_from_tag(entry['tag'], stamp_root)
        entry = dict(entry)
        entry['stamp_files'] = sf
    return entry

